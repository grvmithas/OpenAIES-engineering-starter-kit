import os
from deepeval import assert_test
from deepeval.metrics import AnswerRelevancyMetric
from deepeval.test_case import LLMTestCase

# =====================================================================
# OAIES Standard LLM Evaluation Test Case
# Documented in: 07-llmops/evaluation/framework-selection.md
# Run using: deepeval test run eval/
# =====================================================================

def test_summary_relevancy():
  """
  Verifies that a generated summary is semantically relevant to the input document.
  Uses DeepEval's AnswerRelevancyMetric (an LLM-as-a-judge metric) to compute a score.
  """
  
  # 1. Define the input prompt and output from your LLM model
  user_input = (
    "Summarize the Harness Principle. The Harness Principle states that "
    "an LLM is a probabilistic engine, while the surrounding wrapper (harness) "
    "must be deterministic, managing safety, logging, validation, and execution."
  )
  
  # This represents the output generated by your system's prompt execution
  actual_output = (
    "The Harness Principle requires surrounding probabilistic LLM reasoning "
    "with a deterministic software harness that handles execution, safety, and validation."
  )
  
  # 2. Package it into a standard DeepEval Test Case
  test_case = LLMTestCase(
    input=user_input,
    actual_output=actual_output,
    retrieval_context=[
      "The Harness Principle: Wrap probabilistic models with deterministic code."
    ]
  )
  
  # 3. Configure the metric (LLM-as-a-judge model evaluates this on a scale of 0-1)
  # A threshold of 0.7 means a score below 70% relevance will fail the test.
  relevancy_metric = AnswerRelevancyMetric(threshold=0.7)
  
  # 4. Assert correctness to run the evaluation gate
  assert_test(test_case, [relevancy_metric])
